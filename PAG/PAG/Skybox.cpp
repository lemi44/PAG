	glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);

	glBufferData(GL_ARRAY_BUFFER, 36 * 3 * sizeof(float), &skyboxVertices[0], GL_STATIC_DRAW);

	// vertex positions
